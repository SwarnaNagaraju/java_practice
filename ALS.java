import java.util.*;
public class ALS{
    public static void main(String[] args){
        Scanner ss = new Scanner(System.in);
        int i,n;
        n = ss.nextInt();
        for(i=0;i<n;i++){
            System.out.print(i);
        }
        for(i=0;i>n;i--){
            System.out.print(i);
        }
    }
}